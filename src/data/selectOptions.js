/**
 * 将请求到的数据转换成select需要的数据
 * @param dataArray
 * @param labelKey
 * @param valueKey
 * @returns {Array}
 */
export function transform (dataArray = [], labelKey, valueKey) {
  let transformArray = [{ label: '全部', value: 'all' }]
  dataArray.forEach((data) => {
    transformArray.push({ label: data[labelKey], value: data[valueKey] })
  })
  return transformArray
}
